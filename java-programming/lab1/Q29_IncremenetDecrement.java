package com.company;

public class Q29_IncremenetDecrement {
    public static void main(String[] args) {
        //increment operator
        int n=1;
        System.out.println(n);
        n++;
        System.out.println(n);
        //Decrement Operator
        int m=1;
        System.out.println(m);
        m--;
        System.out.println(m);
    }
}
